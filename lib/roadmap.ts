export const PHASES = {
  aggressive: { label: "Aggressive", range: [1, 12] as const, hex: "#F87171" },
  moderate: { label: "Moderate", range: [13, 27] as const, hex: "#FBBF24" },
  steady: { label: "Steady", range: [28, 57] as const, hex: "#34D399" },
};

export function todayISO(): string {
  const d = new Date();
  return `${d.getFullYear()}-${String(d.getMonth() + 1).padStart(2, "0")}-${String(d.getDate()).padStart(2, "0")}`;
}

export function formatDateShort(iso: string): string {
  const d = new Date(iso + "T00:00:00");
  return d.toLocaleDateString("en-US", {
    weekday: "short",
    month: "short",
    day: "numeric",
  });
}

export function getCurrentWeek(): RoadmapWeek {
  const today = todayISO();
  return ROADMAP.find((w) => today >= w.startDate && today <= w.endDate) || ROADMAP[0];
}

export function getPhase(weekNum: number) {
  if (weekNum <= 12) return PHASES.aggressive;
  if (weekNum <= 27) return PHASES.moderate;
  return PHASES.steady;
}

export function round1(n: number): number {
  return Math.round(n * 10) / 10;
}
